% ChanAI Pulse Prediction rendering regression test.
% Uses synthetic result structs and invisible UI axes; no training is run.
clearvars;
clc;

repoRoot = fileparts(fileparts(mfilename("fullpath")));
addpath(genpath(fullfile(repoRoot, "app")));
addpath(genpath(fullfile(repoRoot, "core")));

fig = uifigure("Visible", "off", "Position", [100, 100, 1200, 720]);
cleanup = onCleanup(@() delete(fig));
layout = uigridlayout(fig, [2, 3]);
axesHandles = struct( ...
    "capacity", uiaxes(layout), ...
    "rmse", uiaxes(layout), ...
    "raw_data", uiaxes(layout), ...
    "spread", uiaxes(layout), ...
    "angular", uiaxes(layout), ...
    "doppler", uiaxes(layout));

predictionResults = struct();
predictionResults.TCN_Sim = fixtureResult(0.0, [1.1, 0.7, 1.4]);
predictionResults.LSTM_Sim = fixtureResult(0.3, [1.2, 0.8, 1.5]);
style = fixtureStyle("EN");

plotData = render_prediction_plots(axesHandles, predictionResults, style);
assert(isequal(plotData.algorithms, {"TCN_Sim", "LSTM_Sim"}), ...
    "Prediction renderer selected the wrong result set.");
assert(plotData.worst_group == 3, "Worst RMSE group changed.");
assert(string(axesHandles.capacity.Title.String) == "[Synthetic] Capacity", ...
    "English capacity title changed.");
assert(string(axesHandles.rmse.Title.String) == "[Synthetic] Group RMSE", ...
    "English RMSE title changed.");
assert(contains(string(axesHandles.raw_data.Title.String), "Worst-Case Group 3"), ...
    "Worst-case PSD title changed.");
assert(string(axesHandles.spread.Title.String) == "[Synthetic] DS CDF", ...
    "English CDF title changed.");
assert(isequal(axesHandles.spread.YLim, [0, 1]), ...
    "Prediction CDF y-axis must remain [0, 1].");
assert(numel(axesHandles.capacity.Children) == 3 && ...
    numel(axesHandles.rmse.Children) == 2 && ...
    numel(axesHandles.raw_data.Children) == 3 && ...
    numel(axesHandles.spread.Children) == 3, ...
    "Prediction charts must render the expected comparison curves.");
assert(isscalar(axesHandles.angular.Children) && ...
    isscalar(axesHandles.doppler.Children), ...
    "Angular and Doppler spectra must render when supplied.");
assert(all(isfinite(plotData.capacity_values)) && all(isfinite(plotData.psd_values)), ...
    "Prediction rendering produced invalid values.");

render_prediction_plots(axesHandles, predictionResults, style);
assert(numel(axesHandles.capacity.Children) == 3 && ...
    numel(axesHandles.raw_data.Children) == 3, ...
    "Repeated rendering must replace rather than stack curves.");

style.language = "CN";
style.prefix = "合成";
render_prediction_plots(axesHandles, predictionResults, style);
assert(contains(string(axesHandles.capacity.Title.String), "容量验证"), ...
    "Chinese capacity title changed.");
assert(contains(string(axesHandles.angular.Title.String), "角度功率谱"), ...
    "Chinese angular title changed.");

singleResult = struct("TCN_Sim", fixtureResult(0.0, 0.9));
style.language = "EN";
style.prefix = "Single";
render_prediction_plots(axesHandles, singleResult, style);
assert(string(axesHandles.rmse.Title.String) == "[Single] Overall RMSE", ...
    "Single-group RMSE fallback title changed.");
assert(isscalar(findobj(axesHandles.rmse, "Type", "bar")), ...
    "Single-group RMSE must render as a bar chart.");

missingSpectrumResult = predictionResults;
missingSpectrumResult.TCN_Sim.Metrics.space.psd = -999;
missingSpectrumResult.TCN_Sim.Metrics.time.y = -999;
render_prediction_plots(axesHandles, missingSpectrumResult, style);
assert(~isempty(findobj(axesHandles.angular, "Type", "text")) && ...
    ~isempty(findobj(axesHandles.doppler, "Type", "text")), ...
    "Missing spectra must retain no-data placeholders.");

fprintf("PASS: prediction rendering preserves comparison charts and bilingual states.\n");

function result = fixtureResult(offset, groupRmse)
delayAxis = linspace(0, 40, 8);
snapshots = 20;
rawOriginal = repmat(-82 + 0.15 * delayAxis, snapshots, 1) + ...
    repmat((1:snapshots).', 1, numel(delayAxis)) * 0.02;
result = struct( ...
    "SNR", [-10, 0, 10, 20], ...
    "C_ori", [0.2, 0.8, 1.6, 2.3].', ...
    "C_pre", ([0.25, 0.78, 1.55, 2.28] + offset).', ...
    "CapAcc", 97.5 - offset, ...
    "NRMSE", 4.2 + offset, ...
    "RMSE", 0.45 + offset, ...
    "GroupRMSE", groupRmse, ...
    "Raw_Ori", rawOriginal, ...
    "Raw_Pre", rawOriginal + 0.35 + offset, ...
    "xo", [5, 10, 15, 20], ...
    "fo", [0.2, 0.5, 0.8, 1.0], ...
    "xp", [5, 10, 15, 20], ...
    "fp", [0.18, 0.48, 0.79, 1.0], ...
    "Metrics", struct( ...
        "delay", struct("x", delayAxis), ...
        "space", struct("angle", -60:30:60, "psd", [-12, -6, 0, -6, -12]), ...
        "time", struct("x", [-50, 0, 50], "y", [-15, 0, -15])));
end

function style = fixtureStyle(language)
style = struct( ...
    "language", language, ...
    "dataset_label", "[Simulated] fixture", ...
    "prefix", "Synthetic", ...
    "true_color", [0.2, 0.2, 0.2], ...
    "primary_color", [0.0, 0.447, 0.741], ...
    "tcn_color", [0.0, 0.447, 0.741], ...
    "lstm_color", [0.850, 0.325, 0.098], ...
    "gru_color", [0.466, 0.674, 0.188], ...
    "text_color", [0.1, 0.1, 0.1], ...
    "text_dim_color", [0.5, 0.5, 0.5]);
end
