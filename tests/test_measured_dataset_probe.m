% ChanAI Pulse measured dataset probe.
% This script is read-only with respect to the original archives. It may create
% extracted preview copies under datasets/measured/extracted_preview for audit.
% It does not train, predict, or modify measured data files.

clearvars;
clc;

repoRoot = fileparts(fileparts(mfilename("fullpath")));
measuredRoot = fullfile(repoRoot, "datasets", "measured");
archiveDir = fullfile(measuredRoot, "raw_archives");
previewDir = fullfile(measuredRoot, "extracted_preview");
auditFile = fullfile(measuredRoot, "DATASET_AUDIT.md");

if ~isfolder(archiveDir)
    error("Archive directory not found: %s", archiveDir);
end
if ~isfolder(previewDir)
    mkdir(previewDir);
end

archives = dir(fullfile(archiveDir, "*.zip"));
fprintf("Found %d measured archive(s).\n", numel(archives));

fid = fopen(auditFile, "w", "n", "UTF-8");
cleanupObj = onCleanup(@() fclose(fid));

fprintf(fid, "# Measured Dataset Audit\n\n");
fprintf(fid, "Generated by `tests/test_measured_dataset_probe.m`.\n\n");
fprintf(fid, "This audit inspects archive structure and MATLAB variables without training or prediction.\n\n");

fprintf(fid, "## 1. Overall Dataset Status\n\n");
fprintf(fid, "- Archive directory: `%s`\n", archiveDir);
fprintf(fid, "- Extracted preview directory: `%s`\n", previewDir);
fprintf(fid, "- Archive count: %d\n\n", numel(archives));

allMatRecords = {};
archiveSummaries = struct([]);

fprintf(fid, "## 2. Archive Structure\n\n");

for aIdx = 1:numel(archives)
    archivePath = fullfile(archives(aIdx).folder, archives(aIdx).name);
    [~, archiveBase] = fileparts(archives(aIdx).name);
    extractTarget = fullfile(previewDir, archiveBase);

    fprintf("Inspecting archive: %s\n", archives(aIdx).name);
    if ~isfolder(extractTarget) || isempty(dir(fullfile(extractTarget, "**", "*")))
        fprintf("Extracting preview copy to: %s\n", extractTarget);
        unzip(archivePath, extractTarget);
    end

    files = dir(fullfile(extractTarget, "**", "*"));
    files = files(~[files.isdir]);
    relPaths = strings(numel(files), 1);
    exts = strings(numel(files), 1);
    for i = 1:numel(files)
        fullPath = fullfile(files(i).folder, files(i).name);
        relPaths(i) = erase(string(fullPath), string(extractTarget) + filesep);
        [~, ~, ext] = fileparts(files(i).name);
        if ext == ""
            ext = "(none)";
        end
        exts(i) = lower(string(ext));
    end

    matFiles = files(lower(string(arrayfun(@(f) getExt(f.name), files, "UniformOutput", false))) == ".mat");
    archiveSummaries(aIdx).name = archives(aIdx).name; %#ok<SAGROW>
    archiveSummaries(aIdx).sizeBytes = archives(aIdx).bytes;
    archiveSummaries(aIdx).fileCount = numel(files);
    archiveSummaries(aIdx).matCount = numel(matFiles);

    fprintf(fid, "### %s\n\n", archives(aIdx).name);
    fprintf(fid, "- Size: %.2f MB\n", archives(aIdx).bytes / 1024 / 1024);
    fprintf(fid, "- File count after preview extraction: %d\n", numel(files));
    fprintf(fid, "- MATLAB `.mat` files: %d\n", numel(matFiles));
    fprintf(fid, "- Extracted preview root: `%s`\n\n", extractTarget);

    fprintf(fid, "**File type counts**\n\n");
    [uExts, ~, groups] = unique(exts);
    counts = accumarray(groups, 1);
    for i = 1:numel(uExts)
        fprintf(fid, "- `%s`: %d\n", uExts(i), counts(i));
    end
    fprintf(fid, "\n**Top-level / sample structure**\n\n");
    sampleCount = min(40, numel(relPaths));
    for i = 1:sampleCount
        fprintf(fid, "- `%s`\n", relPaths(i));
    end
    if numel(relPaths) > sampleCount
        fprintf(fid, "- ... %d more files\n", numel(relPaths) - sampleCount);
    end
    fprintf(fid, "\n");

    for mIdx = 1:numel(matFiles)
        matPath = fullfile(matFiles(mIdx).folder, matFiles(mIdx).name);
        relMatPath = erase(string(matPath), string(extractTarget) + filesep);
        record = inspectMatFile(matPath, archives(aIdx).name, relMatPath);
        allMatRecords{end+1} = record; %#ok<SAGROW>
    end
end

fprintf(fid, "## 3. MATLAB Data Files\n\n");
fprintf(fid, "Total `.mat` files inspected: %d\n\n", numel(allMatRecords));

maxDetailed = numel(allMatRecords);
for i = 1:maxDetailed
    r = allMatRecords{i};
    fprintf(fid, "### %d. `%s` / `%s`\n\n", i, r.archive, r.relativePath);
    if r.error ~= ""
        fprintf(fid, "- Read status: ERROR - %s\n\n", r.error);
        continue;
    end
    fprintf(fid, "- Variable count: %d\n", numel(r.variables));
    fprintf(fid, "- Detected tags: %s\n", strjoin(r.tags, ", "));
    fprintf(fid, "- Direct App compatibility estimate: %s\n", r.compatibility);
    fprintf(fid, "- Meaning inference: %s\n\n", r.inference);
    fprintf(fid, "| Variable | Type | Size | Complex | Notes |\n");
    fprintf(fid, "| --- | --- | --- | --- | --- |\n");
    for vIdx = 1:numel(r.variables)
        v = r.variables(vIdx);
        fprintf(fid, "| `%s` | `%s` | `%s` | %s | %s |\n", ...
            v.name, v.class, v.sizeText, boolText(v.isComplex), v.notes);
    end
    fprintf(fid, "\n");
end

fprintf(fid, "## 4. Variable Notes\n\n");
fprintf(fid, "- Names containing `SAGE` or struct fields such as `delay`, `angle`, `power`, `tau`, or `path` usually indicate extracted multipath parameters.\n");
fprintf(fid, "- Names containing `CIR`, `IR`, or `h` may represent time-domain channel impulse response data, but this must be confirmed from source documentation.\n");
fprintf(fid, "- Names containing `CTF`, `H`, or frequency markers may represent frequency-domain channel transfer functions or channel matrices.\n");
fprintf(fid, "- PDP/DPSD-like variables may already be delay-power data suitable for characterization plots.\n\n");

fprintf(fid, "## 5. Compatibility with Current Platform\n\n");
fprintf(fid, "Current App functions considered: `extract_raw_data`, `calculate_angular_spectrum`, and `analyzeChannelData_Generic`.\n\n");
fprintf(fid, "- `extract_raw_data` can unwrap structs/cells and recognizes fields including `DPSD_dB`, `DPSD_cut`, `sage`, `cir`, `CIRData`, `IRuse`, and `input`.\n");
fprintf(fid, "- SAGE-style files may be readable if they contain a `sage` field whose first element has a numeric `cir` field.\n");
fprintf(fid, "- Numeric real arrays can usually enter `analyzeChannelData_Generic` after shape normalization.\n");
fprintf(fid, "- Complex arrays can support angular spectrum estimation, but scenario-specific conversion may still be required.\n");
fprintf(fid, "- Files that only contain parameter structs without a direct numeric CIR/DPSD-like array likely need a converter.\n\n");

fprintf(fid, "## 6. Recommendations\n\n");
fprintf(fid, "- Keep raw measured archives local until publication permission and dataset licensing are confirmed.\n");
fprintf(fid, "- Do not place full measured archives under `demo_data/`.\n");
fprintf(fid, "- Create small synthetic demos separately for GitHub.\n");
fprintf(fid, "- For real measured data, add a converter layer in a later phase rather than changing App training/prediction logic.\n");
fprintf(fid, "- Candidate future public dataset track: `ChanAIs Dataset`, with metadata, schema, citation, and licensing handled independently.\n");

fprintf("Audit written to: %s\n", auditFile);

function ext = getExt(name)
[~, ~, ext] = fileparts(name);
ext = lower(ext);
end

function record = inspectMatFile(matPath, archiveName, relPath)
record = struct();
record.archive = string(archiveName);
record.relativePath = string(relPath);
record.variables = struct("name", {}, "class", {}, "sizeText", {}, "isComplex", {}, "notes", {});
record.tags = strings(0);
record.compatibility = "Unknown";
record.inference = "Unable to determine confidently from names alone.";
record.error = "";

try
    info = whos("-file", matPath);
catch ME
    record.error = string(ME.message);
    return;
end

nameText = lower(string(relPath));
tagSet = strings(0);

for i = 1:numel(info)
    v = struct();
    v.name = string(info(i).name);
    v.class = string(info(i).class);
    v.sizeText = strjoin(string(info(i).size), "x");
    v.isComplex = logical(info(i).complex);
    v.notes = "";

    lname = lower(v.name);
    if contains(lname, "sage") || contains(nameText, "sage")
        tagSet(end+1) = "SAGE"; %#ok<AGROW>
    end
    if contains(lname, "cir") || contains(lname, "iruse") || contains(lname, "impulse")
        tagSet(end+1) = "CIR"; %#ok<AGROW>
    end
    if contains(lname, "ctf") || strcmpi(v.name, "H") || contains(lname, "freq")
        tagSet(end+1) = "CTF / Channel Matrix"; %#ok<AGROW>
    end
    if contains(lname, "pdp")
        tagSet(end+1) = "PDP"; %#ok<AGROW>
    end
    if contains(lname, "dpsd")
        tagSet(end+1) = "DPSD"; %#ok<AGROW>
    end
    if contains(lname, "delay") || contains(lname, "tau")
        tagSet(end+1) = "Delay"; %#ok<AGROW>
    end
    if contains(lname, "angle") || contains(lname, "aoa") || contains(lname, "aod")
        tagSet(end+1) = "Angle"; %#ok<AGROW>
    end
    if contains(lname, "doppler") || contains(lname, "fd")
        tagSet(end+1) = "Doppler"; %#ok<AGROW>
    end

    if info(i).bytes < 5e6 && (info(i).class == "struct" || info(i).class == "cell")
        try
            loaded = load(matPath, info(i).name);
            value = loaded.(info(i).name);
            v.notes = inspectContainerFields(value);
            fieldText = lower(v.notes);
            if contains(fieldText, "cir")
                tagSet(end+1) = "CIR"; %#ok<AGROW>
            end
            if contains(fieldText, "delay") || contains(fieldText, "tau")
                tagSet(end+1) = "Delay"; %#ok<AGROW>
            end
            if contains(fieldText, "angle") || contains(fieldText, "aoa") || contains(fieldText, "aod")
                tagSet(end+1) = "Angle"; %#ok<AGROW>
            end
        catch ME
            v.notes = "Container field inspection failed: " + string(ME.message);
        end
    end

    record.variables(end+1) = v; %#ok<AGROW>
end

if isempty(tagSet)
    record.tags = "No obvious wireless-channel tags detected";
else
    record.tags = unique(tagSet, "stable");
end

allNames = lower(strjoin(string({record.variables.name}), " "));
if contains(allNames, "dpsd") || contains(allNames, "cirdata") || contains(allNames, "iruse") || contains(allNames, "input")
    record.compatibility = "Likely directly readable by extract_raw_data";
elseif contains(allNames, "sage") || contains(lower(string(relPath)), "sage")
    record.compatibility = "Possibly readable if sage contains numeric cir; otherwise converter needed";
elseif any([record.variables.class] == "double" | [record.variables.class] == "single")
    record.compatibility = "May be readable if first numeric variable is channel-like";
else
    record.compatibility = "Converter likely needed";
end

if contains(lower(string(relPath)), "f28")
    bandText = "likely 28 GHz mmWave";
elseif contains(lower(string(relPath)), "f7")
    bandText = "possibly 7 GHz / upper Sub-6-adjacent or low microwave; confirm metadata";
else
    bandText = "frequency band not clear";
end

if contains(lower(string(relPath)), "moving") || contains(lower(string(relPath)), "road") || contains(lower(string(relPath)), "道路")
    sceneText = "mobile road / moving receiver measurement suggested by filename";
else
    sceneText = "scenario not clear";
end

if any(contains(record.tags, "SAGE"))
    dataText = "SAGE multipath parameter data";
elseif any(contains(record.tags, "CIR"))
    dataText = "time-domain CIR-like data";
elseif any(contains(record.tags, "CTF"))
    dataText = "frequency-domain channel matrix / CTF-like data";
else
    dataText = "channel data type not clear";
end

record.inference = sprintf("%s; %s; %s.", sceneText, bandText, dataText);
end

function notes = inspectContainerFields(value)
fields = strings(0);
if iscell(value) && ~isempty(value)
    value = value{1};
end
if isstruct(value)
    fields = string(fieldnames(value));
    if numel(value) > 1
        value = value(1);
    end
    for i = 1:numel(fields)
        try
            child = value.(fields(i));
            if isstruct(child)
                childFields = string(fieldnames(child));
                fields = [fields; fields(i) + "." + childFields(:)]; %#ok<AGROW>
            end
        catch
        end
    end
end
if isempty(fields)
    notes = "No shallow fields";
else
    maxFields = min(20, numel(fields));
    notes = "Fields: " + strjoin(fields(1:maxFields), ", ");
    if numel(fields) > maxFields
        notes = notes + sprintf(" ... %d more", numel(fields) - maxFields);
    end
end
end

function text = boolText(value)
if value
    text = "yes";
else
    text = "no";
end
end
